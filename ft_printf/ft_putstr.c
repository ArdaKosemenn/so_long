#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (s == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	i = 0;
	while (s[i] != 0)
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
